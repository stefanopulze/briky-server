package com.stefano.briky.json;

public class CategoryJson {

    private int id;
    private String name;
    private String slug;

    public CategoryJson() {
    }

    public CategoryJson(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }
}
